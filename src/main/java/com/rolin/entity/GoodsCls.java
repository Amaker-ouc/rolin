package com.rolin.entity;

public class GoodsCls {
    private Integer clsId;

    private Integer goodsId;

    private String goodsClsPic;

    private String goodsClsName;

    private Float goodsClsPrice;

    public Integer getClsId() {
        return clsId;
    }

    public void setClsId(Integer clsId) {
        this.clsId = clsId;
    }

    public Integer getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(Integer goodsId) {
        this.goodsId = goodsId;
    }

    public String getGoodsClsPic() {
        return goodsClsPic;
    }

    public void setGoodsClsPic(String goodsClsPic) {
        this.goodsClsPic = goodsClsPic;
    }

    public String getGoodsClsName() {
        return goodsClsName;
    }

    public void setGoodsClsName(String goodsClsName) {
        this.goodsClsName = goodsClsName;
    }

    public Float getGoodsClsPrice() {
        return goodsClsPrice;
    }

    public void setGoodsClsPrice(Float goodsClsPrice) {
        this.goodsClsPrice = goodsClsPrice;
    }
}